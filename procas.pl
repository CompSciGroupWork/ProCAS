%formula for calculating cluster points
formula(S_1, S_2, S_3, S_4, OverallMarks, Points):-
	Sum is (S_1+S_2+S_3+S_4),
	X is (Sum/48) * (OverallMarks/84),
	Sq is sqrt(X),
	Points is Sq * 48.
