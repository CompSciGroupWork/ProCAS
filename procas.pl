%Rule to get user preferences
get_prefs(Field, Years):-
	%Get user preferences
	write('Enter Preffered Field (see "procas_help(fields)." for assistance): '),
	read(IntField), nl,
	map_int_field(IntField, Field),
	write('Enter Preffered Course Duration (in Years):'),
	read(Years), nl.

%Main rule that initiates ProCAS
procas:-
	procas_help(intro), nl,
	%Get overall Marks
	write("Enter Overall Marks: "),
	read(OverallMarks), nl,
	%Get preferences
	get_prefs(Field, Years),
	%Obtain a filtered list of courses based on these preferences
	filter_field_years(Field, Years, CourseList),
	%format('Courses with Field: ~w, Years: ~w', [Field, Years]), nl,
	%write(CourseList), nl,
	extract_group(CourseList, Groups),
	list_withoutlast(Groups, CourseGroups),
	%Call sort/2 to remove duplicate groups
	sort(CourseGroups, GroupsSorted),
	%write("Extracted groups: "), write(GroupsSorted), nl, nl,
	calculate_cluster(OverallMarks, GroupsSorted, ClusterPoints),
	list_withoutlast(ClusterPoints, ClusterPointsWithoutLast),
	write("ClusterPoints: "), write(ClusterPointsWithoutLast), nl,
	nth0(0, GroupsSorted, G), 
	recommend(G, Field, Years, FinalRecommendations),nl, nl,
	map_field_name(Field, UserFriendlyField),
	format("**For courses in the field '~w', Duration ~0f Years **", [UserFriendlyField, Years]), nl,
	write("**ProCAS Recommends: **"),nl,
	print_list(5, FinalRecommendations).
	%Recommend courses 
%recommend(GroupsSorted, Field, Years, FinalRecommendations)
recommend(Group, Field, Years, FinalRecommendations):-
	findall(Name, course(Name, Group, Field, Years), FinalRecommendations).

extract_group([], _):-1 is 1. %Base case, must be true
extract_group([H|T], [H1|T1]):-
	nth0(1, H, Group),
	write(Group), nl,
	H1 = Group,
	extract_group(T, T1).
	%L = H1.
	%write("Groups:"), write(L), nl.

calculate_cluster(OverallMarks, [], [H1|T1]):- 1 is 1.
calculate_cluster(OverallMarks, [H|T], [H1|T1]):-
	Group = H,
	write("Recommendations fall in Group "), write(Group), nl,
	writeinfo(Group), nl,
	format("*Group ~0f Subjects*", Group), nl,
	readinfo(S1,S2,S3,S4),
	formula(S1,S2,S3,S4,OverallMarks, Points),
	H1 = Points,
	calculate_cluster(OverallMarks, T, T1).
	%write("Points: "), write(H1), nl.


